	<h2 class="h2">Phystically Uncloneable Functions</h2>
	<h4 class="h4">Introduction</h4>
	This Paper, addresses the pervasive challenges associated with counterfeit luxury items and theft in
	the high-end consumer electronics market. In a more specific context, we propose a solution centered
	around Physical Unclonable Function (PUF) hardware for authentication. This system includes a mobile
	application that empowers users to verify product integrity and a backend server for secure data storage
	and verification.
	<br /><br />
	The project was a collaborative effort, written with Thor Andresen and Mathias Laursen, under the guidance
	of Niels Olof Bouvin, with a focus on enhancing security and integrity in the high-end consumer electronics
	sector.
	<h4 class="h4">Abstract -</h4>
	In the world of luxury goods and high-end consumer electronics, the threat of theft and counterfeiting
	looms large, endangering both consumers and manufacturers. Traditional security measures, such as certificates
	of ownership and serial numbers, have proven vulnerable to manipulation and theft.
	<br /><br />
	This thesis presents a novel solution based on Physical Unclonable Function (PUF) technology to address
	these challenges. PUFs rely on challenge-response pairs (CRP) that resist brute force attacks and replication
	attempts, providing robust authentication. To make this technology accessible to consumers, a mobile
	application is developed, empowering users to easily verify the authenticity and integrity of physical
	products. Complementing the PUF-based authentication system is a backend server, responsible for verifying
	product integrity, state, and history. This data is securely stored in a database, ensuring the accuracy
	of product information.
	<br /><br />
	The proposed system is designed from the perspective of manufacturers, specifically targeting the market
	of high-end consumer electronics, such as mobile speakers with price ranges spanning from 5000 to 30000
	Danish Kroner. This architecture can be adapted for use in other premium consumer electronics segments,
	offering enhanced security and integrity, benefiting both end consumers and manufacturers. With this
	innovative approach, the landscape of luxury goods and high-end electronics may see a transformation,
	reducing the threats of theft and counterfeiting, and enhancing overall product trustworthiness.
</section>

