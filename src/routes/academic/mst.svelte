	<h2 class="h2">Master Thesis</h2>

	<h3 class="h3">Exploring Non-Fungible Tokens (NFTs)</h3>
	<h4 class="h4">Summary</h4>
	<span>
		This master thesis delves into the realm of Non-Fungible Tokens (NFTs), distributed system and
		their diverse applications. NFTs have gained recognition across various domains, from simple
		image-based projects to complex systems such as voting, IoT authentication, and ticketing
		services. While businesses acknowledge the potential of unique tokens, the average consumer's
		understanding and appreciation of NFTs remain limited.
		<br /><br />
		A preliminary questionnaire involving 123 participants shed light on the prevailing perception of
		NFTs. While people grasp the concept of uniqueness, they often view NFTs as mass-produced images
		marketed as investment opportunities, which fosters skepticism. The immaterial nature of NFTs compounds
		this skepticism.
		<br /><br />
		The primary contribution of this thesis is the FUDOT framework, which serves as a comprehensive survey
		and categorization of various NFT projects. The framework identifies shared attributes and illustrates
		them with real-world examples. The subsequent chapters delve into the FUDOT framework's development
		and analysis.
		<br /><br />
		Through this framework, a gap in NFT technology is discovered, particularly regarding Tangible Interfaces.
		Research and business projects utilizing Tangible Interfaces with NFTs are underexplored, despite
		the potential to empower users and engage them in physical exploration and sociospatial configurations.
		<br /><br />
		The identified gap between Tangible Interfaces and NFTs, along with the findings from the preliminary
		questionnaire, shapes the central research question of this thesis: "What are the benefits and limitations
		of using Tangible User Interfaces as an interaction medium for NFTs, and can physical representations
		enhance the perceived sense of ownership of NFTs?"
		<br /><br />
		The subsequent sections of this thesis explore the development and evaluation of Terragon, a project
		aimed at addressing this research question. Terragon bridges the gap in the FUDOT framework and offers
		a physical representation of an NFT, enabling direct interaction with the token. Through user studies
		and extended evaluations, Terragon challenges users' perceptions of NFTs and their sense of ownership
		by introducing bodily interactions and uniqueness to the NFT experience.
	</span>

